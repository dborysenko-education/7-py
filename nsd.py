def max_div(a, b):
    while b:
        a, b = b, a % b
    return a

# Зчитування двох цілих чисел від користувача
int_1 = int(input("Введіть перше ціле число: "))
int_2 = int(input("Введіть друге ціле число: "))

# Знаходження та виведення найбільшого спільного дільника
nsd = max_div(int_1, int_2)
print(f"Найбільший спільний дільник чисел {int_1} і {int_2} дорівнює {nsd}")